package day19_loops02;

public class SlowlyPrintMyNumbers {
	public static void main(String[] args) throws InterruptedException {
		
		/*
		 * write a program 
		 * 
		 */
		
		int num=1;
		while(num<=10) {
			System.out.print(num + " ");
			num++;
			Thread.sleep(2000);
		}


	
	
	
	
	
	
	
	
	
	
	
	
	}
}