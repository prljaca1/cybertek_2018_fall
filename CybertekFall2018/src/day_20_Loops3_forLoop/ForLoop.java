package day_20_Loops3_forLoop;

public class ForLoop {
	public static void main(String[] args) {
		
	for(int i=1; i <= 5; i++ ) {// with decrement (i--) this conditions is gonna go infinite
		System.out.print(i);// in the same line is gonna print with increment number happy birthday 
		System.out.println("Happy Birthday!");
		}
	
	for( int j = 5; j<=100; j+=5) {  //j+=50->just 2 times
		System.out.println("Congratulations! - " + j);
		}
	

	}
}
