package day_3_VariablesAndType;

public class PrimitivesShow {
	public static void main(String[] args) {
		//byte variables
		byte b1 = 100;
		byte b2 = 127;

		//short variables
		short sh1 = 10000;
		short sh2 = -23456;

		//int variables
		int i1 = 123000;
		int i2 = -64554353;

		//long variables
		long l1 = -2352342342L;
		long l2 = 34534534212l;

		//float variables
		float f1 = 5642324.65F;
		float f2 = 1.1f;

		//double variables
		double d1 = -534543.23;
		double d2 = 23423.1;

		//char variable
		char ch1 = 'A';
		char ch2 = '%';

		//boolean variables
		boolean bl1 = true;
		boolean bl2 = false;

		System.out.println(b1);
		System.out.println(b2);
		System.out.println(sh1);
		System.out.println(sh2);
		System.out.println(i1);
		System.out.println(i2);
		System.out.println(l1);
		System.out.println(l2);
		System.out.println(f1);
		System.out.println(f2);
		System.out.println(d1);
		System.out.println(d2);
		System.out.println(ch1);
		System.out.println(ch2);
		System.out.println(bl1);
		System.out.println(bl2);
	}
}
