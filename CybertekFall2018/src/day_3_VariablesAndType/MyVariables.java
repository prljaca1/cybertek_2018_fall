package day_3_VariablesAndType;

public class MyVariables {
	public static void main(String[] args){
		int apples = 10;
		System.out.println(apples);
		apples = 20;
		System.out.println(apples);

		int mangoes = apples;
		System.out.println(mangoes);

		System.out.println("ApplesCount "+apples);

	}
}

